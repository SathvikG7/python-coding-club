score = int(input("Enter your paper percentage: "))
if score >= 90:
    print("You got a grade 9")
    
elif score >= 80:
    print("You got a grade 8")
    
elif score >= 70:
    print("You got a grade 7")
    
elif score >= 60:
    print("You got a grade 6")
    
else:
    print("You got a grade 5")
